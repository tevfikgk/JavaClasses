package com.class9;

import java.util.Scanner;

public class ForSwitchLoop {
	
	public static void main(String[] args) {
		for (int x = 5; x <= 25; x=x+5) {
			 
			  // Switch on number.
			 
			  switch (x) {
			 
			  case 5: {
			 
			 System.out.println("The case 5 is true");
			 
			  break;
			 
			  }
			  }
			}
	
	
	
	
	
	
	
}
}
