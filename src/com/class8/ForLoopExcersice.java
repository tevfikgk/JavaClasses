public class ForLoopExcersice {
public static void main(String[] args) {
	
	for (int i=21; i<=50; i+=2) {
		System.out.print(" "+ i+ " ");
		}
		//second way
		System.out.println();
	
	for (int i=21; i<=50; i++) {
			if (i%2==1) {
				System.out.print(" "+ i + " ");
			}
		}	
	
	}
}

